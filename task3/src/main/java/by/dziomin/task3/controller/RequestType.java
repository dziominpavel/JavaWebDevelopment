package by.dziomin.task3.controller;

/**
 * Enum class of handled request type.
 */
public enum RequestType {
    /**
     * Read text from file request type.
     */
    READ_TEXT_FROM_FILE,
    /**
     * Concatenate to string request type.
     */
    CONCATENATE_TO_STRING,
    /**
     * Sort request type.
     */
    SORT,
    /**
     * Change locale request type.
     */
    CHANGE_LOCALE
}
